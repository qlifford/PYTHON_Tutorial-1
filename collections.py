# Collections in Python is as single "variable" that can hold multiple values.
# Lists = a list is created by placing items inside [] seperated by commas
#       lists are ordered and changable(mutable) and accepts duplicate values
#       can store data of different types i.e strings, integers, floats, etc.
# Tuples = a tuple is created by placing items inside () seperated by commas
#       tuples are ordered and unchangable and accepts duplicate values
# Set = a set is created by placing items inside {} seperated by commas
#       sets are unordered and unindexed and does not accept duplicate values, but can add/remove items
# Dictionary = a dictionary is created by placing items inside {} seperated by commas
#           dictionaries are unordered, changable and indexed, no duplicate values



